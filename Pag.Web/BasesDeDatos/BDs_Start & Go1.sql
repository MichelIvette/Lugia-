CREATE TABLE CONTRATACION (
	TIPO_CONTRATACION VARCHAR (10) NOT NULL PRIMARY KEY,
	PRECIO INT NOT NULL,
	DESC_BENEFICIOS VARCHAR (50) NOT NULL
