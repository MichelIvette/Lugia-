CREATE TABLE EMPLEADOS (
	RFC_EMP VARCHAR (13) NOT NULL PRIMARY KEY,
	NOMB_EMP VARCHAR(20),
	AP_EMP VARCHAR (20) NOT NULL,
	AM_EMP VARCHAR (20) NOT NULL,
	PUESTO VARCHAR (20) NOT NULL,
